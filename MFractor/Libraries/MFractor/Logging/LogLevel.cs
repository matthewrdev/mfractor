namespace MFractor.Logging
{
    public enum LogLevel
    {
        /// <summary>
        /// 'V' - Verbose (lowest priority).
        /// </summary>
        Verbose,

        /// <summary>
        /// 'D' - Debug
        /// </summary>
        Debug,

        /// <summary>
        /// 'I' - Info
        /// </summary>
        Information,

        /// <summary>
        /// 'W' - Warning
        /// </summary>
        Warning,

        /// <summary>
        /// 'E' - Error
        /// </summary>
        Error,

        /// <summary>
        /// 'F' - Fatal
        /// </summary>
        Fatal,
    }
}

